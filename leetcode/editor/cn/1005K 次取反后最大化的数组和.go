func largestSumAfterKNegations(nums []int, k int) (ret int) {
	abs := func(a int) int {
		if a > 0 {
			return a
		}
		return -a
	}
	sort.Ints(nums)
	cnt := math.MaxInt32
	for i := range nums {
		if nums[i] < 0 && k > 0 {
			ret -= nums[i]
			k--
		} else {
			ret += nums[i]
		}
		if x := abs(nums[i]); x < cnt {
			cnt = x
		}
	}
	if k%2 != 0 {
		ret -= 2 * cnt
	}
	return
}


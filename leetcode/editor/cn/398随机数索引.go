type Solution struct {
	nums []int
}

func Constructor(nums []int) Solution {
	return Solution{nums: nums}
}

func (this *Solution) Pick(target int) (ret int) {
	cnt := 0
	for i, v := range this.nums {
		if target == v {
			cnt++
			if rand.Intn(cnt)+1 == cnt {
				ret = i
			}
		}
	}
	return
}


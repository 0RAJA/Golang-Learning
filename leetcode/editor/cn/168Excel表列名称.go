func convertToTitle(columnNumber int) string {
	var ret []byte
	for columnNumber > 0 {
		columnNumber-- //å¾€å·¦åç§»ä¸€ä½
		ret = append(ret, byte(columnNumber%26+'A'))
		columnNumber /= 26
	}
	for i, n := 0, len(ret); i < n/2; i++ {
		ret[i], ret[n-1-i] = ret[n-1-i], ret[i]
	}
	return string(ret)
}


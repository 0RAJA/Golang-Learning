func getMaximumGold(grid [][]int) (ret int) {
	next := [4][2]int{{0, -1}, {0, 1}, {1, 0}, {-1, 0}}
	var dfs func(x, y, k int)
	dfs = func(x, y, k int) {
		t := grid[x][y]
		grid[x][y] = 0
		k += t
		for i := range next {
			nx, ny := next[i][0]+x, next[i][1]+y
			if nx >= 0 && ny >= 0 && nx < len(grid) && ny < len(grid[nx]) && grid[nx][ny] > 0 {
				dfs(nx, ny, k)
			}
		}
		if ret < k {
			ret = k
		}
		grid[x][y] = t
	}
	for i, row := range grid {
		for j, v := range row {
			if v > 0 {
				dfs(i, j, 0)
			}
		}
	}
	return
}


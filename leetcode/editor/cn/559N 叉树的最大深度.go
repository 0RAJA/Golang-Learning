/**
* Definition for a Node.
* type Node struct {
*     Val int
*     Children []*Node
* }
 */

func maxDepth() int {
	type Node struct {
		Val      int
		Children []*Node
	}
	var root = new(Node)
	var maxDepth func(root *Node) (ret int)
	maxDepth = func(root *Node) (ret int) {
		if root == nil {
			return 0
		}
		for _, p := range root.Children {
			if k := maxDepth(p); k > ret {
				ret = k
			}
		}
		return ret + 1
	}
	return maxDepth(root)
}

